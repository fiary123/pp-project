def init_knowledge_base(reset: bool = False):
    """
    将知识条目批量写入 ChromaDB。
    reset=True 时先清空集合再重写（用于更新知识库）。
    """
    client = chromadb.PersistentClient(path=CHROMA_DB_PATH)

    if reset:
        try:
            client.delete_collection("pet_knowledge")
            print("已清空旧知识库集合。")
        except Exception:
            pass

    collection = client.get_or_create_collection(
        name="pet_knowledge",
        metadata={"hnsw:space": "cosine"}  # 使用余弦相似度
    )

    # 检查已有条目数量
    existing = collection.count()
    print(f"当前知识库条目数：{existing}")

    # 获取已存在的 ID，避免重复插入
    if existing > 0:
        existing_ids = set(collection.get()["ids"])
    else:
        existing_ids = set()

    new_entries = [e for e in KNOWLEDGE_ENTRIES if e["id"] not in existing_ids]

    if not new_entries:
        print(f"所有 {len(KNOWLEDGE_ENTRIES)} 条知识已存在，无需重复写入。")
        return existing

    # 批量写入
    ids = [e["id"] for e in new_entries]
    documents = [e["text"] for e in new_entries]
    metadatas = [
        {k: v for k, v in e.items() if k not in ("id", "text")}
        for e in new_entries
    ]

    # ChromaDB 每批最多 166 条（避免超限），按批写入
    BATCH_SIZE = 100
    for i in range(0, len(new_entries), BATCH_SIZE):
        batch_ids = ids[i:i + BATCH_SIZE]
        batch_docs = documents[i:i + BATCH_SIZE]
        batch_meta = metadatas[i:i + BATCH_SIZE]
        collection.add(ids=batch_ids, documents=batch_docs, metadatas=batch_meta)
        print(f"  写入 {len(batch_ids)} 条（{i+1}-{i+len(batch_ids)}）...")

    total = collection.count()
    print(f"\n✅ 知识库初始化完成！共 {total} 条知识条目。")
    print(f"   分类统计：")
    for cat in ["breed_wiki", "health_care", "feeding_guide", "adoption_knowledge", "daily_care"]:
        cat_entries = [e for e in KNOWLEDGE_ENTRIES if e.get("category") == cat]
        print(f"   - {cat}: {len(cat_entries)} 条")
    return total


def query_knowledge_base(keyword: str, n_results: int = 3):
    """测试查询：验证知识库能否正常检索"""
    client = chromadb.PersistentClient(path=CHROMA_DB_PATH)
    collection = client.get_or_create_collection(name="pet_knowledge")

    if collection.count() == 0:
        print("知识库为空，请先运行 init_knowledge_base()")
        return

    results = collection.query(
        query_texts=[keyword],
        n_results=min(n_results, collection.count()),
        include=["documents", "metadatas", "distances"]
    )

    docs = results["documents"][0]
    distances = results["distances"][0]
    metas = results["metadatas"][0]

    print(f"\n🔍 查询：「{keyword}」\n{'='*60}")
    for i, (doc, dist, meta) in enumerate(zip(docs, distances, metas)):
        print(f"\n[{i+1}] 相似度: {1-dist:.3f} | 分类: {meta.get('category')} | 品种: {meta.get('breed', meta.get('condition', meta.get('topic', '-')))}")
        print(f"   {doc[:120]}...")


if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(description="宠物知识库管理工具")
    parser.add_argument("--reset", action="store_true", help="清空并重建知识库")
    parser.add_argument("--query", type=str, default="", help="测试查询关键词")
    args = parser.parse_args()

    if args.query:
        query_knowledge_base(args.query)
    else:
        init_knowledge_base(reset=args.reset)
